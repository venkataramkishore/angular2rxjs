/**
 * Week model contains week info 
 * weeks:{w1:0, w2:0, w3:0, w4:0}}
 */
export class Week {
    constructor(
        public w1:string,
        public w2:string,
        public w3:string,
        public w4:string
    ) {
        this.w1 = w1;
        this.w2 = w2;
        this.w3 = w3;
        this.w4 = w4;
    }
}
